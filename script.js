  function getDominantColor(imageElement) {
    return new Promise((resolve) => {
      console.log('주요 색상 추출 시작:', imageElement.src);
      
      const canvas = document.createElement('canvas');
      const ctx = canvas.getContext('2d');
      const img = new Image();
      
      img.crossOrigin = 'anonymous';
      img.onload = function() {
        console.log('이미지 로드 성공, 크기:', img.width, 'x', img.height);
        
        canvas.width = img.width;
        canvas.height = img.height;
        ctx.drawImage(img, 0, 0);
        
        try {
          const imageData = ctx.getImageData(0, 0, canvas.width, canvas.height);
          const data = imageData.data;
          
          // 색상 빈도 계산
          const colorCounts = {};
          const step = Math.max(1, Math.floor(data.length / 10000)); // 샘플링 간격
          
          for (let i = 0; i < data.length; i += step * 4) {
            const r = Math.round(data[i] / 20) * 20;
            const g = Math.round(data[i + 1] / 20) * 20;
            const b = Math.round(data[i + 2] / 20) * 20;
            const key = `${r},${g},${b}`;
            colorCounts[key] = (colorCounts[key] || 0) + 1;
          }
          
          // 가장 빈번한 색상 찾기
          let maxCount = 0;
          let dominantColor = null;
          
          for (const [color, count] of Object.entries(colorCounts)) {
            if (count > maxCount) {
              const [r, g, b] = color.split(',').map(Number);
              // 밝기 계산
              const brightness = (r * 299 + g * 587 + b * 114) / 1000;
              // 적절한 밝기 범위의 색상만 선택
              if (brightness > 30 && brightness < 220) {
                maxCount = count;
                dominantColor = `rgb(${r}, ${g}, ${b})`;
              }
            }
          }
          
          console.log('주요 색상 추출 완료:', dominantColor, '밝기:', dominantColor ? (parseInt(dominantColor.match(/\d+/g)[0]) * 299 + parseInt(dominantColor.match(/\d+/g)[1]) * 587 + parseInt(dominantColor.match(/\d+/g)[2]) * 114) / 1000 : 'N/A');
          resolve(dominantColor || '#ededed');
        } catch (e) {
          console.error('주요 색상 추출 오류:', e);
          resolve('#ededed');
        }
      };
      
      img.onerror = function() {
        console.error('이미지 로드 실패:', imageElement.src);
        resolve('#ededed');
      };
      
      img.src = imageElement.src;
    });
  }

